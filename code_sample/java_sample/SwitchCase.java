package code_sample.java_sample;

public class SwitchCase {
    public static void main(String[] args) {
        int key = 2;

        switch (key) {
            case 1: 
                System.out.println("Case 1");
                break;
            case 2:
                System.out.println("Case 2");    
                break;
        
            default:
                break;
        }
    }
}